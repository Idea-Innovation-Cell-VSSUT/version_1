In 1901 a group of women in Harvard university were classifying the types of stars on the basis of the substance they were made up of. Annie Cannon joined a group of female astronomers nicknamed "Pickering's Women." The team, which included Williamina P. S. Fleming, worked to document and empirically classify stars. Annie Jump Cannon provided our understanding of the substance of the stars and another woman working with her knew  the way to calculate the size of the universe. Spectrum of the stars were compared to the elements present on earth and to their surprise they found these stars were also made up of the same element which also existed on earth. It took decades to classify the spectral characters of the stars. There were seven types of stars and these categories had subcategories as they varied minutely.<br><br>
In England 1923 ,women were forbidden for pursuing advanced degrees in Science. But Cecillia Payne wasn’t going to be chained down by superstitions and dogmas in the society. Her interest in astronomy began after she attended a lecture by Arthur Eddington on his 1919 expedition to the island of Principe in the Gulf of Guinea off the west coast of Africa to observe and photograph the stars near a solar eclipse as a test of Albert Einstein's general theory of relativity. She said of the lecture: 'The result was a complete transformation of my world picture.’ She shifted to America where women were already given the freedom to study stars. She examined the chemical content and physical state of the data taken by Cannon. In 1924 Henry Norris who was the dean of American astronomers said that a major contribution of the 42-45 elements on earth are known to be present on the surface of the Sun  also but Payne contradicted it and said that the stars are made up of hydrogen and Helium million times more than other elements.However, when Payne's dissertation was reviewed, astronomer Henry Norris Russell, who stood by the theories of American physicist Henry Rowland,felt sorry for Payne and then said that her laws were fundamentally incorrect four years after then her laws were proved to be correct. <br><br>
