One of the most massive stars known as the Eta. Carinae has a very different fate. Its fate is a hypernova. It lies 7,500 light-years from Earth. It will go out more like an adolescent suicide bomber, blazing through its nuclear fuel in a mere couple of million years and exploding as a supernova, a blast so violent that its flash will briefly outshine the entire Milky Way. The corpse that this kind of cosmic detonation leaves behind is a black hole. After the explosion it will emit harmful radiation. Some scientists say it won't affect earth as it is 7500 light years away. Others say that the explosion will be so evil that it will end all life on earth.
